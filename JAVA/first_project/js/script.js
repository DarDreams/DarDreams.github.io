// fib(1) => "0"
// fib(0) => ''"

function fib(int) {
    const fibArray = [0, 1];
      let result = "";
    if (int == 1) {
        // console.log("0") ;
        result = "0";
        return result;   
    }
    if (typeof(int) !== 'number' || int === 0 || !Number.isInteger(int) ){
        // console.log("blank space");
        result = "";
        return result;    
    }
    else {
        for (let i = 2; i < int; i++) {
            fibArray[i] = fibArray[i-1]+fibArray[i-2];
        }
        for (let k = 0; k < int; k++) {
            result += " "+fibArray[k]; 
        }
        // console.log('result =', "|"+result.trim()+"|");
        return(result.trim());
    }
}
fib(7.2);


